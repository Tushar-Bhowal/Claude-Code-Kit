#!/usr/bin/env python3
"""PreToolUse(Bash) hook: block git commits that contain AI/model attribution.

Reads the tool-call JSON on stdin. If Claude is about to run a `git commit`
whose message contains AI/model attribution, it writes a reason to stderr and
exits 2, which blocks the commit and feeds the reason back to Claude so it can
rewrite the message. Any other command, or a parse failure, exits 0 (allow).
"""
import json
import re
import sys

try:
    data = json.load(sys.stdin)
except Exception:
    sys.exit(0)  # can't parse -> never block on uncertainty

if data.get("tool_name") != "Bash":
    sys.exit(0)

cmd = (data.get("tool_input", {}) or {}).get("command", "") or ""

# Only inspect git commit commands.
if not re.search(r"\bgit\b.*\bcommit\b", cmd, re.IGNORECASE | re.DOTALL):
    sys.exit(0)

# Attribution signatures to block (case-insensitive). Edit this list freely.
PATTERNS = [
    r"generated with",
    r"generated by",
    r"co-?authored-?by:\s*claude",
    r"co-?authored-?by:.*anthropic",
    r"noreply@anthropic\.com",
    r"🤖",
    r"with claude code",
    r"written by claude",
    r"using claude",
    r"ai[-\s]generated",
    r"claude code",
    # For maximum strictness, uncomment these (may false-positive in
    # repos that legitimately mention Claude/Anthropic in commit text):
    # r"\bclaude\b",
    # r"\banthropic\b",
]

for pat in PATTERNS:
    if re.search(pat, cmd, re.IGNORECASE):
        sys.stderr.write(
            "Blocked: the commit message contains AI/model attribution "
            f"(matched: '{pat}'). Rewrite it as a human engineer would — no "
            "mention of Claude, AI, an assistant, or the model; no 'Generated "
            "with' footer; no Co-Authored-By trailer. Then commit again.\n"
        )
        sys.exit(2)

sys.exit(0)
